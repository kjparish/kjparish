// Add Mobile Body Class "wp-is-mobile" for mobile
add_filter( 'body_class', 'mobile_detect_class' );
function mobile_detect_class( $classes ) {
	
	/* using mobile browser */
		if ( wp_is_mobile() ){
			$classes[] = 'is-mobile';
		}
	   else{
			$classes[] = 'is-desktop';
		}

	return $classes;
	
}
